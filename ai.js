async function getAIReply(history, customerName) {
  // Монголын цаг (UTC+8)
  const now = new Date();
  const mongolHour = (now.getUTCHours() + 8) % 24;

  const systemWithVars = SYSTEM_PROMPT
    .replace(/\[CUSTOMER_NAME\]/g, customerName || "")
    .replace(/\[CURRENT_HOUR\]/g, mongolHour.toString());

  const response = await client.messages.create({
    model: "claude-sonnet-4-6",
    max_tokens: 1000,
    system: systemWithVars,
    messages: history,
  });

  return response.content[0].text;
}

module.exports = { getAIReply };

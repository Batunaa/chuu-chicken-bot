async function getAIReply(history, customerName) {
  const systemWithName = SYSTEM_PROMPT.replace(/\[CUSTOMER_NAME\]/g, customerName || "");

  const response = await client.messages.create({
    model: "claude-sonnet-4-6",
    max_tokens: 1000,
    system: systemWithName,
    messages: history,
  });

  return response.content[0].text;
}

